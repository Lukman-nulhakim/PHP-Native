Buat class dengan nama SMK
di class SMK buat property = nama, umur, alamat, kelas (public)
                           = jurusan (private)
                           = angkatan (protected)


ada class turunan Siswa dan Siswi



contoh tampil di browser:

Anto umur 18 alamat di cipondoh kelas 12 jurusan teknik mesin 2015 -> class Siswa

Reni umur 16 alamat di ciledug kelas 10 jurusan tata boga 2017 -> class Siswa



contoh stuktur :

class SMK{
    semua property di smk
}


class Siswa extends SMK{
    method siswa()
}

class Siswi extends SMK{
    method siswa()
}

